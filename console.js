// log to console
// log a string
console.log("Hello World");
// log numbers
console.log(123);
// log booleans
console.log(true);
// create and log variables
var greeting = "hello";
console.log(greeting);
// log arrays
console.log([1, 2, 3, 4]);
// log objects
console.log({ a: 1, b: 2 });
// log objects as tables
console.table({ a: 1, b: 2 });
// log to error stream
console.error("This is some error");
// clear the console
console.clear();
// log to warning stream
console.warn("This is some warning");
// start console timer
console.time();
for (let i = 0; i < 10; i++) {
  console.log("Hello World");
}
// end console timer
console.timeEnd();

